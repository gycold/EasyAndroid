    public static boolean makeDirs(String filePath) {
        String folderName = getFolderName(filePath);
        if (StringUtil.isEmpty(folderName)) {
            return false;
        }
        File folder = new File(folderName);
        return (folder.exists() && folder.isDirectory()) ? true : folder.mkdirs();
    }

    /**
     * 通过给定路径取得文件目录
     * 例子：
     * getFolderName(null)                       =   null
     * getFolderName("")                         =   ""
     * getFolderName("   ")                      =   ""
     * getFolderName("a.mp3")                    =   ""
     * getFolderName("a.b.rmvb")                 =   ""
     * getFolderName("abc")                      =   ""
     * getFolderName("c:\\")                     =   "c:"
     * getFolderName("c:\\a")                    =   "c:"
     * getFolderName("c:\\a.b")                  =   "c:"
     * getFolderName("c:a.txt\\a")               =   "c:a.txt"
     * getFolderName("c:a\\b\\c\\d.txt")         =   "c:a\\b\\c"
     * getFolderName("/home/admin")              =   "/home"
     * getFolderName("/home/admin/a.txt/b.mp3")  =   "/home/admin/a.txt"
     *
     * @param filePath
     * @return
     */
    public static String getFolderName(String filePath) {
        if (StringUtil.isEmpty(filePath)) {
            return filePath;
        }
        int filePosi = filePath.lastIndexOf(File.separator);
        return (filePosi == -1) ? "" : filePath.substring(0, filePosi);
    }

    /**
     * 根据文件路径，将指定的字符串内容写入文件
     *
     * @param filePath 文件路径
     * @param content  指定字符串
     * @param append   是否是附加内容，true：写入文件的尾部，false：写入文件的开头
     * @return 如果content为空则返回false
     */
    public static boolean writeFile(String filePath, String content, boolean append) {
        if (StringUtil.isEmpty(content)) {
            return false;
        }

        FileWriter fileWriter = null;
        try {
            makeDirs(filePath);
            fileWriter = new FileWriter(filePath, append);
            fileWriter.write(content);
            return true;
        } catch (IOException e) {
            throw new RuntimeException("IOException occurred.", e);
        } finally {
            closeIO(fileWriter);
        }
    }

    /**
     * 根据文件路径，将指定的字符串序列内容写入文件
     *
     * @param filePath    文件路径
     * @param contentList 字符串序列
     * @param append      是否是附加内容，true：写入文件的尾部，false：写入文件的开头
     * @return 如果content为空则返回false
     */
    public static boolean writeFile(String filePath, List<String> contentList, boolean append) {
        if (contentList.size() == 0 || null == contentList) {
            return false;
        }

        FileWriter fileWriter = null;
        try {
            makeDirs(filePath);//根据文件路径创建目录
            fileWriter = new FileWriter(filePath, append);
            int i = 0;
            for (String line : contentList) {
                if (i++ > 0) {
                    fileWriter.write("\r\n");
                }
                fileWriter.write(line);
            }
            return true;
        } catch (IOException e) {
            throw new RuntimeException("IOException occurred. ", e);
        } finally {
            closeIO(fileWriter);
        }
    }

